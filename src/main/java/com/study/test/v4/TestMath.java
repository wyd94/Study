package com.study.test.v4;

public class TestMath {
	public static void main(String[] args){
		//整型运算
		int i1=9;
		int i2=2;
		System.out.println("i1="+i1+",i2="+i2);
		System.out.println("i1+i2="+(i1+i2));//加
		System.out.println("i1-i2="+(i1-i2));//减
		System.out.println("i1*i2="+(i1*i2));//乘
		System.out.println("i1/i2="+(i1/i2));//除
		System.out.println("i1%i2="+(i1%i2));//取余数
		
		//double类型运算
		double d1=8.0;
		double d2=2.5;
		System.out.println("d1="+d1+",d2="+d2);
		System.out.println("d1+d2="+(d1+d2));//加
		System.out.println("d1-d2="+(d1-d2));//减
		System.out.println("d1*d2="+(d1*d2));//乘
		System.out.println("d1/d2="+(d1/d2));//除
		
		
	}
}
